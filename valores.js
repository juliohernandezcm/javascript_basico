// Valor tipo number
40;

// Valor tipo string
("Julio Hernandez");

// Valores booleanos
true;
false;

// Valores vacios
null;
undefined;

/* VALORES NO PRIMITIVOS O TIPO OBJETOS  */

// Valor tipo array
[1, 2, 3];

// Valor tipo objeto
{
  nombre: "Julio";
}

// Como podemos corrobar el tipo de valor
typeof 40; //number
typeof "Julio"; // string
typeof false; // boolean
typeof true; // boolean
typeof []; // object
